// 获取指定等级的成长系数（Lv1-120 之间线性插值；超出范围 clamp）
export function getGrowthRate(level) {
  const lv = Math.max(1, Math.min(120, Math.floor(level)));
  return GROWTH_RATES[lv] || GROWTH_RATES[1];
}

// 等级缩放系数：相对 Lv90 的比例（Lv90 是模拟器基准等级）
// 用于 combat.js 中敌人属性缩放
export function growthRatioTo90(level) {
  const cur = getGrowthRate(level);
  const base90 = GROWTH_RATES[90];
  return {
    hp: cur.hp / base90.hp,
    atk: cur.atk / base90.atk,
    def: cur.def / base90.def,
  };
}
